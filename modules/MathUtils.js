// MathUtils class contains simple math functions
class MathUtils {
    // Adds two numbers and returns the result
    add(a, b) {
        return a + b;
    }

    // Subtracts the second number from the first and returns the result
    subtract(a, b) {
        return a - b;
    }
}

// Export MathUtils class so it can be used in other files
module.exports = MathUtils;
